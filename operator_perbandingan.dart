void main() {
  ///pemrograman ini biasanya berfungsi
  ///untuk perbandingan seperti 'boolean'

  print(10 == 10); //sama dengan
  print(10 != 10); //tidak sama dengan
  print(10 < 10); //kurang dari
  print(10 > 10); //besar dari
  print(10 <= 10); //kurang dari sama dengan
  print(10 >= 10); //besar dari sama dengan

  ///ini juga dapat berlaku untuk string dan juga int

  print('david' == 'david');
  print('david' != 'david');
}
